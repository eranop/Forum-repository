    public void increaseForums(){
        Integer x=Integer.parseInt(forumsSizeLabel.getText());
        forumsSizeLabel.setText((++x).toString());
    }
    public void decreaseForums(){
        Integer x=Integer.parseInt(forumsSizeLabel.getText());
        forumsSizeLabel.setText((--x).toString());
    }
    
    
    public void setForum(String forum) throws RemoteException{
        _ci.enterForum(forum);
    }
    
