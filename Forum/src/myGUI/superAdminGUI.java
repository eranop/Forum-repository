    private void crreateForumButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_crreateForumButtonActionPerformed
        if(forumIssueTextField.getText().equals("") || descriptionTextArea.getText().equals("")){
            reports.setText("empty fields. please insert issue and description");
        }else{
            try {
                _ci.exitForum();
                report r;
                r = _ci.createForum(forumIssueTextField.getText(), descriptionTextArea.getText());
                reports.setText("creating forum " + forumIssueTextField.getText() + " : " + r.toString());
                if(r==report.OK){
                    forumsComboBox.addItem(forumIssueTextField.getText());
                    increaseForums();
                    forumIssueTextField.setText("");
                    descriptionTextArea.setText("");
                }
            } catch (RemoteException ex) {
                Logger.getLogger(superAdminGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_crreateForumButtonActionPerformed

    private void deleteForumButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteForumButtonActionPerformed
        String forum=(String) forumsComboBox.getSelectedItem();
        try {
            report r=_ci.deleteForum(forum);
            reports.setText("delete forum " + forum + ": " + r.toString());
            if(r==report.OK){
                forumsComboBox.removeItem(forum);
                decreaseForums();
            }
        } catch (RemoteException ex) {
            Logger.getLogger(superAdminGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_deleteForumButtonActionPerformed

    private void forumsComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_forumsComboBoxActionPerformed
        String forum= (String) forumsComboBox.getSelectedItem();
        try {
            _ci.exitForum();
            _ci.enterForum(forum);
            Vector<String> admins=_ci.getAdministratorsVector(forum);
            Vector<String> simpleMembers=_ci.getMembersOfForum(forum);
            simpleMembers.removeAll(admins);
            adminList.setListData(admins.toArray());
            membersList.setListData(simpleMembers.toArray());
            
        } catch (RemoteException ex) {
            Logger.getLogger(superAdminGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_forumsComboBoxActionPerformed

